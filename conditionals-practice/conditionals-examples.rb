# 'If' is used in much the same way as js 

# a simple if statement with a 'puts' which is like a console.log in js

if 2 > 1 
    puts "2 is greater than 1"
end

# let's give this a bit more bite by using variables and more conditions:
# first, we assign values:

ten = 10
any_number = 42

# Then some conditionals! 
if ten < any_number === true
    puts "ten is the lower number"
elsif ten > any_number === true
    puts "ten is the higher number"
else 
    puts "ten is equal to any_number"
end 

